CREATE VIEW v_ToolRatings AS
SELECT
    t.tool_name,
    AVG(r.rating) AS average_rating,
    COUNT(r.review_id) AS review_count
FROM
    Tools t
LEFT JOIN Reviews r ON t.tool_id = r.tool_id
GROUP BY
    t.tool_name;

